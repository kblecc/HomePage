      <h4>The Ideal and Styling Process</h4>
      <p>
        The project began with a clear objective: to challenge the prevailing image of Poon Choi as
        "heavy and traditional." The team aimed to create a "retro & light" vibe that would feel
        both nostalgic and fresh. The core styling idea was to use a minimalist cartoon design
        combined with fresh, vibrant colors. This approach was chosen specifically to:
      </p>

      <ul>
        <li>
          <b>Transform the traditional image</b><br />
          Move away from the often dark and dense visuals associated with Poon Choi.
        </li>
        <li>
          <b>Create a relaxed atmosphere</b><br />
          Make the dish feel more approachable, fun, and suitable for casual gatherings.
        </li>
        <li>
          <b>Attract a younger audience</b><br />
          Use a contemporary aesthetic that resonates with young people and encourages them to try
          Poon Choi.
        </li>
      </ul>

